// console.log()
//-prints the string written inside the quotation marks.
// Math.sqrt()
//-Returns the square root of a number.
// Math.pow()
//-Returns the value of a base raised to a power.
// Math.abs()
//-Returns the absolute value of a number.
//toUpperCase()
//-Retunts the string converted to uppercase.
//toLowerCase()
//-Returns the string converted to lowercase.

//Math.sqrt() computes the square root
let squareRoot = Math.sqrt(4);
console.log(`Square root of 4 is: ${squareRoot}`); // Output: Square root of 4 is: 2
//Math.pow() computes the power of a number
let power = Math.pow(2, 3);
console.log(`2 raised to the power of 3 is: ${power}`); // Output: 2 raised to the power of 3 is: 8
//Math.abs() computes the absolute value
let absoluteValue = Math.abs(-5);
console.log(`Absolute value of -5 is: ${absoluteValue}`); // Output: Absolute value of -5 is: 5
//toUpperCase() converts a string to uppercase
let upperCaseString = "hello".toUpperCase();
console.log(`Uppercase of "hello" is: ${upperCaseString}`); // Output: Uppercase of "hello" is: HELLO
//toLowerCase() converts a string to lowercase
let lowerCaseString = "WORLD".toLowerCase();
console.log(`Lowercase of "WORLD" is: ${lowerCaseString}`); // Output: Lowercase of "WORLD" is: world
